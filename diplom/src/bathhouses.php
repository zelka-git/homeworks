<section class="bath-description">
  <div class="container">
    <h3 class="bath-description__title">Русская баня - отдых душой и телом!</h3>
    <p class="bath-description__paragraph">Русская баня давно завоевала мировое признание. Миллионы людей постоянно прибегают к этому простому и надежному способу поддерживать здоровье, красоту, жизненный тонус и хорошее настроение. В X веке в Киево-Печерском монастыре монахи, узнав о пользе бани из древних книг, собранных в библиотеке, стали устраивать бани для лечебных целей, но на русский манер. В уставе великого князя Владимира бани именовались заведениями для немогущих. О русской бане упоминается в летописях X — XII веков. Ее называли мовь, мовня, мовница, мыльня, влазня или просто баня. В Древней Руси побежденные племена даже платили дань березовыми вениками.</p>
    <p class="bath-description__paragraph">На Руси культ русской бани существует очень и очень давно. Традиционную русскую парную готовят тщательно: моют пол, открывают дверь – парная должна быть чистой и проветренной, чтобы жар был легким и мягким. В русской бане при средней температуре 60-70 градусов, влажность составляет 80 %. Для создания духа парной используют настои трав: мята, эвкалипт, пихтовое и хвойное масло. Что очень полезно для легких и бронхов.</p>
    <p class="bath-description__paragraph">Париться в русской бане принято сидя или лежа. Голову принято накрывать полотенцем или специальной шапочкой, чтобы волосы не пересушились и не стали ломкими. Первый заход в парилку разогревающий, он не более пяти минут. В первое посещение сходит самый грязный и едкий пот. В последующие разы можно сидеть в парной до первых признаков усталости. Банные гурманы посещают парную за два часа, раз пять, но неподготовленному человеку за это время хватит и трех. После каждого посещения парной можно принять холодный душ, или скупаться в деревянной катке, наполненной холодной водой. Контрастные процедуры очень полезны для кожи и для общего тонуса организма. Особенно полезно это в зимнее время, когда необходимо поднять иммунитет и сопротивляемость к простудным болезням.</p>
    <p class="bath-description__paragraph">Похлестывание веником тела – это своеобразный массаж, усиливающий кровообращение, способствующий последующему контрасту температур, более интенсивному потоотделению и усилению обмена веществ. Воздействие на кожу эфирными маслами, содержащимися в листьях веника, нейтрализует действие различных болезнетворных микробов, и препятствуют преждевременному старению кожи.</p>
